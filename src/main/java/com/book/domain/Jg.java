package com.book.domain;

public class Jg {

    private int jgId;
    private String jgName;
    private String jgReason;
    private int jgState;

    public int getJgId() {
        return jgId;
    }

    public void setJgId(int jgId) {
        this.jgId = jgId;
    }

    public String getJgName() {
        return jgName;
    }

    public void setJgName(String jgName) {
        this.jgName = jgName;
    }

    public String getJgReason() {
        return jgReason;
    }

    public void setJgReason(String jgReason) {
        this.jgReason = jgReason;
    }

    public int getJgState() {
        return jgState;
    }

    public void setJgState(int jgState) {
        this.jgState = jgState;
    }

}
